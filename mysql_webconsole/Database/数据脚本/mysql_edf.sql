DROP TABLE IF EXISTS `SYS_NAVIGATOR`;
CREATE TABLE `SYS_NAVIGATOR` (
  `NAV_ID` int(11) NOT NULL AUTO_INCREMENT COMMENT '菜单id'  ,
  `NAV_NAME` varchar(50) COMMENT '菜单名称'  ,
  `NAV_URL` varchar(500) COMMENT '菜单url'  ,
  `NAV_SEQ` int(11) COMMENT '菜单序号'  ,
  `NAV_NOTE` varchar(255) COMMENT '菜单备注'  ,
  `NAV_STATUS` int(11)  COMMENT '菜单状态:0-停用 1-启用' ,
  PRIMARY KEY  (`NAV_ID`)
) ENGINE=INNODB DEFAULT CHARSET=utf8 COMMENT='导航菜单';


DROP TABLE IF EXISTS `SYS_MENU`;
CREATE TABLE `SYS_MENU` (
  `MENU_ID` int(11)  NOT NULL AUTO_INCREMENT COMMENT '菜单id' ,
  `NAV_ID` int(11)   NOT NULL  COMMENT '菜单id'  ,
  `MENU_TITLE` varchar(50) COMMENT '菜单标题'  ,
  `MENU_CODE` varchar(50) COMMENT '菜单编码'  ,
  `MENU_URL` varchar(500) COMMENT '菜单url'  ,
  `MENU_SEQ` int(11) COMMENT '菜单序号'  ,
  `MENU_STATUS` int(11)  COMMENT '菜单状态:0-停用 1-启用' ,
  `MENU_PARENT_ID` int(11) COMMENT '菜单父节点id'  ,
  `MENU_NOTE` varchar(255) COMMENT '菜单备注'  ,
  PRIMARY KEY  (`MENU_ID`),
  KEY `REFERENCE_1_FK` USING BTREE  (`NAV_ID`) ,
  CONSTRAINT `FK_SYS_MENU_R_SYS_NAVI` FOREIGN KEY (`NAV_ID`) REFERENCES `SYS_NAVIGATOR` (`NAV_ID`) ON DELETE CASCADE 
) ENGINE=INNODB DEFAULT CHARSET=utf8 COMMENT='树形菜单';

DROP TABLE IF EXISTS `TB_IF_C_PARAM_CONF`;
